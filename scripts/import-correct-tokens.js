async function updateTeams() {
  try {
    console.log('Updating teams with correct values...\n')

    for (const [teamId, data] of Object.entries(teamsData)) {
      console.log(`Updating ${teamId}...`)

      await client
        .patch(teamId)
        .set({
          brandColors: data.brandColors,
          buttonRadius: data.buttonRadius,
          displayFont: data.displayFont,
        })
        .commit()

      console.log(`  ✓ Updated ${teamId}`)
    }

    console.log('\n✅ All teams updated successfully!')
  } catch (error) {
    console.error('Error updating teams:', error)
    process.exit(1)
  }
}

updateTeams()
